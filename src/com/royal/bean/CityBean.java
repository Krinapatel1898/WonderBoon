package com.royal.bean;

public class CityBean 
{

	private int cid;
	private String cname;
	private int sid;
	private String sname;
	public CityBean() {
		super();
	}
	public CityBean(String cname, int sid) {
		super();
		this.cname = cname;
		this.sid = sid;
	}
	
	public CityBean(int cid, String cname) {
		super();
		this.cid = cid;
		this.cname = cname;
	}
	
	
	public CityBean(int cid, String cname, int sid) {
		super();
		this.cid = cid;
		this.cname = cname;
		this.sid = sid;
	}
	public CityBean(int cid, String cname, int sid,String sname) {
		super();
		this.cid = cid;
		this.cname = cname;
		this.sid = sid;
		this.sname=sname;
	}
	
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}

}





























                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        
                        








































































                                                                                       
           




      




















                                                    











































